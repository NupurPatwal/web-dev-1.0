/*Switch Statement:
Write a program to print the name of the day of the week based on a number input (1 for Monday, 2 for Tuesday, etc.).*/
let dayNo = 3;
switch (dayNo) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("tuesday");
    break;
  case 3:
    console.log("wedday");
    break;
  case 4:
    console.log("thrusday");
    break;
  case 5:
    console.log("friday");
    break;
  case 6:
    console.log("satday");
    break;
  case 7:
    console.log("Sunday");
    break;

  default:
    break;
}
